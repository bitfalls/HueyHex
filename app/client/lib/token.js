Token.getBalance = function(address, callback)  {
    var balance = 0;

    if(!web3.isAddress(address)) {
        callback("Not valid address",balance);
     } else {
        Token.tokenCon.balanceOf.call(address, function(error,res) {
            if(!error) {
                balance = res*10**-18;
                callback(error,balance);
            }
            else{
              callback(error,balance);
            }
            
        });
    }
  };
  




  
